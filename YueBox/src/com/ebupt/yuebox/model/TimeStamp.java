package com.ebupt.yuebox.model;

public class TimeStamp {
	private long WOTimeStamp;	//工单信息时间戳
	private long TSTimeStamp;	//用户信息时间戳
	
	public TimeStamp()
	{
		
	}
	public TimeStamp(long wOTimeStamp, long tSTimeStamp) {
		WOTimeStamp = wOTimeStamp;
		TSTimeStamp = tSTimeStamp;
	}
	public long getWOTimeStamp() {
		return WOTimeStamp;
	}
	public void setWOTimeStamp(long wOTimeStamp) {
		WOTimeStamp = wOTimeStamp;
	}
	public long getTSTimeStamp() {
		return TSTimeStamp;
	}
	public void setTSTimenStamp(long tSTimeStamp) {
		TSTimeStamp = tSTimeStamp;
	}
}
